/*0. STRING MANIPULATION: Strings can be changed or interpreted through operators or methods
*/

/*1. With operators: strings can be concatenated with + and += operators
*/

console.log("Hello" + " World!") // Hello World!

var x = "Hello"
x += " World!" //Can be read as x = x + World!
console.log(x) //Hello World!

/*2. With string methods:Strings can also be manipulated with methods, similar to arrays.
*There are numerous methods which can be seen here:
*https://www.w3schools.com/jsref/jsref_obj_string.asp
*/ 

var x = "HELLO"
x.toLowerCase() //hello
